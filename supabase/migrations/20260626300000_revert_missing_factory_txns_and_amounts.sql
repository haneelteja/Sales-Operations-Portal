-- ─────────────────────────────────────────────────────────────────────────────
-- STEP 2: Restore production amounts to original trigger behaviour
-- (latest pricing row per SKU, regardless of transaction date).
-- Undoes the LATERAL-join recalculation from 20260626000000.
-- ─────────────────────────────────────────────────────────────────────────────
UPDATE public.factory_payables fp
SET    amount = ROUND((fp.quantity * pr.cost_per_case)::numeric, 2)
FROM (
  SELECT DISTINCT ON (sku) sku, cost_per_case
  FROM  public.factory_pricing
  ORDER BY sku, pricing_date DESC
) pr
WHERE pr.sku = fp.sku
  AND fp.transaction_type = 'production'
  AND fp.quantity IS NOT NULL
  AND fp.sku IS NOT NULL;
